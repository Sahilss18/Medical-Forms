export const getFormsByCategory = (categoryId: string): FormType[] => {
  return ALL_FORMS.filter((form) => form.category === categoryId);
};

// Helper function to get form by ID
export const getFormById = (formId: string): FormType | undefined => {
  return ALL_FORMS.find((form) => form.id === formId);
};

// Helper function to get form by code
export const getFormByCode = (code: string): FormType | undefined => {
  return ALL_FORMS.find((form) => form.code === code);
};
